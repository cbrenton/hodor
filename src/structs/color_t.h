/**
 * Holds the red, green, and blue values of a color_t.
 * @author Chris Brenton
 * @date 06/20/2011
 */

#ifndef _COLOR_T_H
#define _COLOR_T_H

struct color_t
{
   // The red component of the color_t.
   double r;

   // The green component of the color_t.
   double g;

   // The blue component of the color_t.
   double b;

};
#endif
