/**
 * Holds the red, green, and blue values of a color.
 * @author Chris Brenton
 * @date 06/20/2011
 */

#ifndef _COLOR_H
#define _COLOR_H

struct color
{
   // The red component of the color.
   double r;

   // The green component of the color.
   double g;

   // The blue component of the color.
   double b;

};
#endif
